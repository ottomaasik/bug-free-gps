void LPS22HH_Init(void) {
    uint8_t buf[2] = {CTRL_REG1, 0x50};  // Register, then value

    if (HAL_I2C_Master_Transmit(&hi2c1, LPS22HH_ADDR, buf, 2, HAL_MAX_DELAY) != HAL_OK) {
        HAL_UART_Transmit(&huart2, (uint8_t*)"Init write failed\r\n", 20, HAL_MAX_DELAY);
    } else {
        HAL_UART_Transmit(&huart2, (uint8_t*)"LPS22HH Init OK\r\n", 18, HAL_MAX_DELAY);
    }
}

float LPS22HH_ReadPressure(void) {
    uint8_t raw[3];
    HAL_I2C_Mem_Read(&hi2c1, LPS22HH_ADDR, PRESS_OUT_XL, I2C_MEMADD_SIZE_8BIT, raw, 3, HAL_MAX_DELAY);

    int32_t raw_pressure = (int32_t)(raw[2] << 16 | raw[1] << 8 | raw[0]);
    return raw_pressure / 4096.0f; // Convert to hPa


}
